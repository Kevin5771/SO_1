# Programa que demuestra la manipulación básica de variables en Python

# Declaración e inicialización de variables
nombre = "Juan"
edad = 25
altura = 1.75
es_estudiante = True

# Imprimir información utilizando variables
print("Nombre:", nombre)
print("Edad:", edad, "años")
print("Altura:", altura, "metros")
print("¿Es estudiante?", es_estudiante)

# Realizar operaciones con variables
nueva_edad = edad + 5
altura_doble = altura * 2

# Imprimir resultados de operaciones
print("Edad después de 5 años:", nueva_edad, "años")
print("Altura duplicada:", altura_doble, "metros")
