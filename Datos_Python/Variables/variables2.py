# Programa que demuestra el uso de variables en operaciones matemáticas y manipulación de cadenas en Python

# Declaración e inicialización de variables
numero1 = 10
numero2 = 5
suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2

# Imprimir resultados de operaciones matemáticas
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)

# Declaración de variables de cadena
saludo = "Hola, "
nombre = "Ana"

# Concatenación de cadenas
mensaje_completo = saludo + nombre

# Imprimir mensaje completo
print(mensaje_completo)
